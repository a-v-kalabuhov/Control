namespace UsrConnector.Core;

/// <summary>
/// Настройки автомата состояния ТПА. Все таймауты адаптивные: вычисляются от средней
/// длительности успешного auto-цикла, умноженной на коэффициент. До накопления статистики
/// используется <see cref="SeedCycleMs"/> (консервативно большое значение: первый зависший
/// цикл на быстрой машине будет детектирован с запозданием, зато без ложных срабатываний
/// на медленной).
/// </summary>
public sealed record StateMachineSettings
{
    /// <summary>
    /// Стартовая оценка длительности цикла, мс, пока не накоплена статистика.
    /// По умолчанию 300 000 мс (300 с) — верх диапазона реальных циклов ТПА (3…300 с).
    /// </summary>
    public double SeedCycleMs { get; init; } = 300_000;

    /// <summary>
    /// Коэффициент alarm-таймаута к средней длительности цикла: если впрыск начался, а
    /// E1 не наступил за (среднее × коэффициент) — цикл считается зависшим (Alarm).
    /// </summary>
    public double AlarmTimeoutCoef { get; init; } = 2.0;

    /// <summary>
    /// Коэффициент idle-таймаута к средней длительности цикла: если после завершения цикла
    /// новый впрыск не начался за (среднее × коэффициент) — станок простаивает (Idle).
    /// </summary>
    public double IdleTimeoutCoef { get; init; } = 3.0;

    /// <summary>Окно скользящего среднего: сколько последних успешных auto-циклов учитывать.</summary>
    public int AverageWindowCycles { get; init; } = 10;

    /// <summary>
    /// Порог длительного простоя, мс, после которого накопленная статистика длительности
    /// цикла сбрасывается (вероятная переналадка: сменили пресс-форму — сменилась и
    /// длительность цикла). Захардкоженное по договорённости значение: 900 000 мс (15 мин).
    /// </summary>
    public double StatisticsResetAfterMs { get; init; } = 900_000;

    /// <summary>
    /// Сколько подряд неудачных опросов считать потерей связи (Offline). Демпфер против
    /// ложных Interrupted на единичной сетевой ошибке: рекомендуется выставлять так, чтобы
    /// суммарное время (число × период опроса) было сравнимо с длительностью цикла.
    /// </summary>
    public int OfflineAfterFailedPolls { get; init; } = 3;

    /// <summary>Текущий alarm-таймаут при заданном среднем.</summary>
    public double AlarmTimeoutMs(double avgCycleMs) => avgCycleMs * AlarmTimeoutCoef;

    /// <summary>Текущий idle-таймаут при заданном среднем.</summary>
    public double IdleTimeoutMs(double avgCycleMs) => avgCycleMs * IdleTimeoutCoef;
}
